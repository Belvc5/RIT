    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // Validamos que las cajas de texto esten llenas
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Nombre del Empleado ");
            txtnombre.requestFocus();
            return;
        }
        if (txtapellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Apellido del Empleado");
            txtapellido.requestFocus();
            return;
        }
        if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un Tipo de Documento para el Empleado");
            txtnum_documento.requestFocus();
            return;
        }
        if (txtsueldo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un Sueldo para el Empleado");
            txtsueldo.requestFocus();
            return;
        }
        
        if (txtlogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un Usuario para el empleado");
            txtlogin.requestFocus();
            return;
        }
        if (txtpassword.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar un Password para el Empleado");
            txtpassword.requestFocus();
            return;
        }
               
        //Instancio la clase Empleado para crear un nuevo objeto
        //Instancio la clase FunEmpleado para llamar a todas las funciones
        Empleado dts = new Empleado();
        FunEmpleado func = new FunEmpleado();
        //Cajas de texto
        dts.setNombre(txtnombre.getText());
        dts.setApellido(txtapellido.getText());
        //Combo box tipo_documento
        int seleccionado = cbtipo_documento.getSelectedIndex();
        dts.setTipo_documento((String)cbtipo_documento.getItemAt(seleccionado));
        //Cajas de texto
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setSueldo(Double.parseDouble(txtsueldo.getText()));
        //Combo box acceso
        seleccionado=cbacceso.getSelectedIndex();
        dts.setAcceso((String)cbacceso.getItemAt(seleccionado));
        //Cajas de texto
        dts.setLogin(txtlogin.getText());
        dts.setPassword(txtpassword.getText());
         //Combo box estado
        seleccionado=cbestado.getSelectedIndex();
        dts.setEstado((String)cbestado.getItemAt(seleccionado));
        
        

        //Guardar o Editar registro
        if (accion.equals("guardar")) {
            //Inserto los datos
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Se registro al Empleado exitosamente");
                //muestro todos los datos
                mostrar("");
                inhabilitar();
            }
        }

        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
            if(func.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "Se edito al Empleado exitosamente");
                //muestro todos los datos
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

