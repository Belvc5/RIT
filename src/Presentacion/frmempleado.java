    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // Editamos una infraccion de la tabla
        btnguardar.setText("Editar");
        //habilito las cajas de texto
        habilitar();
        //Habilito mi boton eliminar
        btneliminar.setEnabled(true);
        accion="editar";

        //Almaceno la fila donde hago clcik de la tabla listado
        int fila = tablalistado.rowAtPoint(evt.getPoint());
        //Obtengo los datos de la columna de la tabla
        txtidpersona.setText(tablalistado.getValueAt(fila, 0).toString());
        txtnombre.setText(tablalistado.getValueAt(fila, 1).toString());
        txtapellido.setText(tablalistado.getValueAt(fila, 2).toString());
        cbtipo_documento.setSelectedItem(tablalistado.getValueAt(fila, 3).toString());
        txtnum_documento.setText(tablalistado.getValueAt(fila, 4).toString());
        txtdireccion.setText(tablalistado.getValueAt(fila, 5).toString());
        txttelefono.setText(tablalistado.getValueAt(fila, 6).toString());
        txtemail.setText(tablalistado.getValueAt(fila, 7).toString());
        txtsueldo.setText(tablalistado.getValueAt(fila, 8).toString());
        cbacceso.setSelectedItem(tablalistado.getValueAt(fila, 9).toString());
        txtlogin.setText(tablalistado.getValueAt(fila, 10).toString());
        txtpassword.setText(tablalistado.getValueAt(fila, 11).toString());
        cbestado.setSelectedItem(tablalistado.getValueAt(fila, 12).toString());

    }//GEN-LAST:event_tablalistadoMouseClicked

