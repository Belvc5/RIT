    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // Verifico que mi caja de texto no este en blanco
        if (!txtidpersona.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de eliminar al Empleado?","Confirmar",2);
            if(confirmacion == 0){
                Empleado dts = new Empleado();
                FunEmpleado func = new FunEmpleado();
                //Envio a mi dts el idpersona
                dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
                //Llamo a mi funcion eliminar
                func.eliminar(dts);
                //metodo mostrar para ver todos
                mostrar("");
                //llamo al procedimiento inhabilitar
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

