    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // TODO add your handling code here:
        try {
            //Creo una table y un instancia de Empleado y FunEmpleado
            DefaultTableModel modelo;
            Empleado dts = new Empleado();
            FunEmpleado func = new FunEmpleado();          
            //Envio al objeto dts todos lo datos de las cajas de texto
            dts.setLogin(txtusuario.getText());
            dts.setPassword(txtpassword.getText());
            //Envio los valores de mi funcion login al modelo
            modelo=func.login(dts.getLogin(), dts.getPassword());
            //Envio el modelo a la tabla listado
            tablalistado.setModel(modelo);
            //Determino si el usuario esta registado >0 existe
            if (func.totalregistros > 0) {
                //Cierro el formulario frmusuariologin
                this.dispose();
                //Hago una instacia al formulario frminicio
                frminicio form = new frminicio();
                form.toFront();
                form.setVisible(true);
                //Envio todos los dato del usuario de la tabla listado a los label
                frminicio.lblidpersona.setText(tablalistado.getValueAt(0,0).toString());
                frminicio.lblnombre.setText(tablalistado.getValueAt(0,1).toString());
                frminicio.lblapellido.setText(tablalistado.getValueAt(0,2).toString());
                frminicio.lblacceso.setText(tablalistado.getValueAt(0,3).toString());
                //Si el acceso es diferente de administrador deshabilito algunos accesos
                if (!frminicio.lblacceso.getText().equals("Administrador")) {
                    frminicio.mnuarchivo.setEnabled(false);
                    frminicio.mnuconfiguraciones.setEnabled(false);
                    
                }
            }
            //Si no esta registrado el usuario
            else{
                JOptionPane.showMessageDialog(rootPane, "Acceso Denegado\nUd no esta registrado en el sistema","Acceso al Sistema",JOptionPane.ERROR_MESSAGE);
            }
                
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btningresarActionPerformed

